private void select_datos(){
    int fila = tb_clientes.getSelectedRow();
        lbl_id.setText(tb_clientes.getValueAt(fila, 0).toString());
        txt_nit.setText(tb_clientes.getValueAt(fila, 1).toString());
        txt_nombres.setText(tb_clientes.getValueAt(fila, 2).toString());
        txt_apellidos.setText(tb_clientes.getValueAt(fila, 3).toString());
        txt_direccion.setText(tb_clientes.getValueAt(fila, 4).toString());
        txt_telefono.setText(tb_clientes.getValueAt(fila, 5).toString());
        txt_fn.setText(tb_clientes.getValueAt(fila, 6).toString());
    
}
